local vida = 10

repeat
    print("Caindo....")
    vida = vida - 2
until vida <= 0

-- VIDA = Declarando variavel, que no caso é = 10

-- REPEAT = significa Repetir, ou seja, ele manda repetir tudo que estiver abaixo de mim
-- UNTIL = significa até, ele executa um bloco de código repetidamente até que uma condição específica se torne verdadeira

-- print("Caindo....") = Mostra a mensagem na tela.
-- vida = vida - 2 = Reduz o valor da vida em 2 unidades a cada rodada.
-- until vida <= 0 = Significa "até que". Aqui o computador faz o teste: "A vida já é menor ou igual a zero?"